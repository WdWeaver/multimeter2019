int cnt = 0;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* h) {
  
  if (h->Instance == TIM4) {        // 300Hz
    //消灯
    panel_blank();
    //消えたら点灯するまで何かやる。
    panel_update_view();
    
  }
  if (h->Instance == TIM3) {
    panel_update_buttons();         //ボタン状態の読出
    panel_measure_transient();      //ボタン状態の更新
  }

}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef* h) {
  if (h->Instance == TIM4) {
    //更新および点灯
    panel_refresh();
  }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
  //EXTI1 triggered
  panel_update_buttons();
}

void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan) {
    canReceiveElapsed = 0;
    CAN_RxHeaderTypeDef hdr = {0};
    uint8_t data[8] = {0,0,0,0,0,0,0,0};
    
    while (HAL_CAN_GetRxFifoFillLevel(hcan,CAN_RX_FIFO0) > 0) {
      HAL_CAN_GetRxMessage(hcan,CAN_RX_FIFO0,&hdr,data);
      switch (hdr.StdId) {
      case 0x0202:
        {
          uint16_t ev = ((data[0] << 8) | data[1]) >> 2;
          panel_updaterpm(ev);
        }
        break;
      default:
        break;
      }
    }
}

